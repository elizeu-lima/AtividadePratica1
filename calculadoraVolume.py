# Crie um programa que calcula o volume de uma caixa retangular. 

comprimento = 12
largura = 14
altura = 20

volume = comprimento * largura * altura

print("3- Calculadora de Volume")
print(f"Comprimento: {comprimento} cm")
print(f"Largura: {largura} cm")
print(f"Altura: {altura} cm")
print(f"Volume: {comprimento} × {largura} × {altura} = {volume} cm³")


